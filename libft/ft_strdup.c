#include "libft.h"

char	*ft_strdup(char *s)
{
	char	*str;
	int		size;
	int		i;

	size = ft_strlen(s);
	str = (char *)malloc(sizeof(char) * (size + 1));
	i = 0;
	if (!str)
		return (0);
	while (s[i])
	{
		str[i] = s[i];
		i++;
	}
	str[i] = '\0';
	return (str);
}
